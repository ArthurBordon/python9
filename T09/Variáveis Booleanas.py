# Variáveis booleanas
aprovado = True
reprovado = False

print(f"Aprovado: {aprovado} - Tipo: {type(aprovado)}")
print(f"Reprovado: {reprovado} - Tipo: {type(reprovado)}")

# Operadores de comparação retornam booleanos
x = 10
y = 20
print(f"x > y: {x > y}")   # False
print(f"x < y: {x < y}")   # True
print(f"x == y: {x == y}") # False