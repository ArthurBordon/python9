print("======== ANALISADOR DE NOME ========")
nome = input("Escreva o seu nome completo: ")
print("======== RESULTADO ========")
print(f" Nome: {nome}")
print(f" Quantidade de caracteres: ", len(nome))
print(f" Primeira letra: ", nome[0])
print(f" Última letra: ", nome[-1])
print(f" Nome em maísculo: ", nome.upper())
print("============================")
